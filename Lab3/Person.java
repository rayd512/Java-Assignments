/**
 * Lab 3: Inheritance, Interfaces, Hash, Design Pattern and Big Number <br />
 * The {@code Person} interface
 */
public class Person {
	// The name of the person
	private String name;

	// Constructor
	public Person(String name) {
		this.name = name;
	}

	// Getter method to get the name
	public String getName() {
		return this.name;
	}
}
